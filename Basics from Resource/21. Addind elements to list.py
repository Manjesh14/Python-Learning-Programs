# append() Method
lang=["c","c++","Java"]
print(lang)
lang.append("python")
print(lang)
lang.append("ruby")
lang.append("Java script")
lang.append("php")
print(lang)
lang.append(["css","C#"])
print(lang)

# insert() Method
lang2=["c","c++","Java"]
lang2.insert(0,"Python")

# extend() method
lang3=["c","c++","Java"]
more_lan=["Python", "JS"]
lang3.extend(more_lan)
print(lang3)