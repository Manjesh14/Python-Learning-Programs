# Greater than (>)
print(10>20)
print(2.5>2.0)

# Lesser than (<)
print(10<20)
print(2.5<2.0)

# Equal to (==)
print(10==20)
print(2.0==2.0)

# Lesser than or Equal to (<=)
print(10<=20)
print(2.5<=2.0)
print(10<=10)

# Greater than or Equal to (>=)
print(10>=20)
print(2.5>=2.0)
print(10>=10)

# Not Equal to (!=)
print(10!=20)
print(2.0!=2.0)