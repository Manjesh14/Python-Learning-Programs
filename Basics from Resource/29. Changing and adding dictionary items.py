# Changing values using key names
car={'brand':'Audi','model':'q7'}
car['model']='s5'
print(car)

# Changing values using update() method
car.update({'model':'s50'})
print(car)

# adding new values using key names
car['color']='Black'
print(car)

# Adding new values using update() method
car.update({'fuel type':'diesel'})
print(car)

