# String Concatenate Operator

print('a'+'b'+'c')

x="I"
y=" am"
z=" Manjesh."
print(x+y+z)

# String Repetition Operator

print(3*"Manjesh ")
print("Manjesh "*3)
print(0*"Manjesh ")
print(-3*"Manjesh ")

#String Comparison Operators

# "==" Operator
print('Hello'=='Hello')
print('Hello'=='hello')

# "!=" Operator
print('Hello'!='Hello')
print('Hello'!='hello')

# "<" Operator
print('Hello'<'hello')
print('hEllo'<'hello')
print('hello'<'Hello')

# "<=" Operator
print('Hello'<='Hello')

# ">" Operator
print('Hello'>'hello')
print('hEllo'>'hello')

#">=" Operator
print('Hello'>='Hello')

# String Membership Operator

# The "in" operator
print('Man' in 'Manjesh')
print('j' in 'Manjesh')
print('jese' in 'Manjesh')

# The "not in" Operator
print('Man' not in 'Manjesh')
print('j' not in 'Manjesh')
print('jse' not in 'Manjesh')

# Escape Sequence Operator
print("I am \"Indian\"")
print("I am Manjesh and \nI am \x66rom \"Indi\141.\"")

#String Formatting Operators
age=18
print("My age is %d"%(age))