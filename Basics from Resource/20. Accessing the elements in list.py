# Accessing the element of single dimensional Lists
num=[5,10,15,20]
print(num[0])
print(num[1])
print(num[2])
print(num[len(num)-1])
print(num[-2])
print(num[-(len(num))])

# Accessing the element of multidimensional Lists
my_list=[[1,2,3],"Neso",[4,5,6]]
print(my_list[0][0])
print(my_list[1])
print(my_list[2][1])