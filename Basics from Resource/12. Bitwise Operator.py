# Bitwise AND operator &
a=10
b=5
print(a & b)
print(bin(a & b))

# Bitwise OR operator |
print(a | b)
print(bin(a | b))

# Bitwise NOT operator ~
print(~a)
print(bin(~a))

# Bitwise XOR operator ^
print(a ^ b)
print(bin(a ^ b))

# Bitwise Right shift  operator (>>)
x=12
print(x>>1)
print(x>>2)
y=-12
print(y>>1)
print(y>>2)

# Bitwise left shift  operator (<<)
c=3
print(c<<1)
print(c<<2)
d=-3
print(d<<1)
print(d<<2)
